export function getShapeById(id: ShapeId): ShapeDef | undefined {
  return GEOMETRY_SHAPES.find((s) => s.id === id);
}

export function getDefaultParams(shape: ShapeDef): Record<string, number> {
  return { ...shape.defaults };
}

export function formatMetric(value: number | null, decimals = 2): string {
  if (value === null || Number.isNaN(value)) return "—";
  if (Math.abs(value) >= 1000) return value.toExponential(2);
  return value.toFixed(decimals);
}
