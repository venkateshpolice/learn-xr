export type ShapeCategory = "basic" | "prism" | "platonic" | "advanced";

export interface ParamDef {
  key: string;
  label: string;
  min: number;
  max: number;
  step: number;
  unit?: string;
  integer?: boolean;
}

export interface ComputedMetric {
  label: string;
  unit: string;
  compute: (p: Record<string, number>) => number | null;
}

export interface ShapeDef {
  id: ShapeId;
  name: string;
  description: string;
  category: ShapeCategory;
  params: ParamDef[];
  defaults: Record<string, number>;
  metrics: ComputedMetric[];
}

